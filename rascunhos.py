    #Classes:
        #atributos = características de um objeto (cor,tamanho...); implementados através de variáveis
        #métodos = ações de um objeto (ligar,acelerar...); implementados através de funções; ex: API (método que aceita requisições)
    #instância = objeto criado a partir de uma classe
    #Pilares de POO:
        #abstração = irrelevância de entender o processo (usar sem saber como funciona)
        #herança = aplicada sempre entre classes (recebendo métodos e atributos)
            #superclasse = doa
            #subclasse = recebe
        #encapsulamento = proteger
            #metodos da mesma classe alteram atributo protegido
        #polimorfismo = dar uma nova forma (reescrever) a um método que recebi como herança, mas mantendo o nome original do método herdado
